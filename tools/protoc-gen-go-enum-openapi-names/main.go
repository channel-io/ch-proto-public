package main

import (
	"fmt"
	"io"
	"log"
	"os"
	"strings"
	"unicode"

	plugin "github.com/golang/protobuf/protoc-gen-go/plugin"
	"github.com/samber/lo"
	"golang.org/x/exp/slices"
	"google.golang.org/protobuf/proto"
	"google.golang.org/protobuf/types/descriptorpb"
	"google.golang.org/protobuf/types/pluginpb"
)

var logger = log.Default()

func watermark() string {
	return "// Code generated by protoc-gen-go-enum-openapi-names. DO NOT EDIT.\n\n"
}

// toScreamingSnake converts PascalCase to SCREAMING_SNAKE_CASE.
// e.g. "GroupScope" → "GROUP_SCOPE", "UserChatState" → "USER_CHAT_STATE"
func toScreamingSnake(s string) string {
	var result []rune
	for i, r := range s {
		if i > 0 && unicode.IsUpper(r) {
			result = append(result, '_')
		}
		result = append(result, unicode.ToUpper(r))
	}
	return string(result)
}

// extractEnumMappings extracts proto enum value name → OpenAPI name (prefix-stripped) mappings.
func extractEnumMappings(
	protoFile *descriptorpb.FileDescriptorProto,
	enum *descriptorpb.EnumDescriptorProto,
) (map[string]string, error) {
	prefix := toScreamingSnake(*enum.Name) + "_"
	unspecified := prefix + "UNSPECIFIED"

	if enum.Value[0].Name == nil {
		return nil, fmt.Errorf("zero value of enum %s.%s is not specified", *protoFile.Package, *enum.Name)
	}

	if *enum.Value[0].Name != unspecified {
		return nil, fmt.Errorf("zero value of enum %s.%s is not %s", *protoFile.Package, *enum.Name, unspecified)
	}

	// proto enum value → OpenAPI name (prefix stripped)
	mapping := map[string]string{}
	for _, value := range enum.Value {
		protoName := *value.Name
		if protoName == unspecified {
			continue // skip UNSPECIFIED
		}

		if !strings.HasPrefix(protoName, prefix) {
			return nil, fmt.Errorf("enum value %s.%s.%s does not have prefix %s", *protoFile.Package, *enum.Name, protoName, prefix)
		}

		openAPIName := strings.TrimPrefix(protoName, prefix)
		mapping[protoName] = openAPIName
	}

	return mapping, nil
}

// generateGoFile generates a Go file with OpenAPIName() method for each enum.
func generateGoFile(
	protoFile *descriptorpb.FileDescriptorProto,
	enums []*descriptorpb.EnumDescriptorProto,
	goPackage string,
) (string, error) {
	var b strings.Builder

	b.WriteString(watermark())
	b.WriteString(fmt.Sprintf("package %s\n\n", goPackage))

	for _, enum := range enums {
		mapping, err := extractEnumMappings(protoFile, enum)
		if err != nil {
			return "", err
		}

		protoNames := lo.Keys(mapping)
		slices.Sort(protoNames)

		// Generate OpenAPIName() method
		b.WriteString(fmt.Sprintf("// OpenAPIName returns the OpenAPI-compatible enum string for %s.\n", *enum.Name))
		b.WriteString(fmt.Sprintf("// Prefix %q is stripped from the proto enum value name.\n", toScreamingSnake(*enum.Name)+"_"))
		b.WriteString(fmt.Sprintf("func (x %s) OpenAPIName() string {\n", *enum.Name))
		b.WriteString("\tswitch x {\n")
		for _, protoName := range protoNames {
			b.WriteString(fmt.Sprintf("\tcase %s_%s:\n", *enum.Name, protoName))
			b.WriteString(fmt.Sprintf("\t\treturn %q\n", mapping[protoName]))
		}
		b.WriteString("\tdefault:\n")
		b.WriteString("\t\treturn \"\"\n")
		b.WriteString("\t}\n")
		b.WriteString("}\n\n")

		// Generate OpenAPINameMap for lookup
		b.WriteString(fmt.Sprintf("// %sOpenAPINames maps proto enum values to OpenAPI names.\n", *enum.Name))
		b.WriteString(fmt.Sprintf("var %sOpenAPINames = map[%s]string{\n", *enum.Name, *enum.Name))
		for _, protoName := range protoNames {
			b.WriteString(fmt.Sprintf("\t%s_%s: %q,\n", *enum.Name, protoName, mapping[protoName]))
		}
		b.WriteString("}\n\n")
	}

	return b.String(), nil
}

func process(req *plugin.CodeGeneratorRequest, resp *plugin.CodeGeneratorResponse) error {
	logger.Println("Applying go-enum-openapi-names plugin ...")

	resp.SupportedFeatures = proto.Uint64(uint64(pluginpb.CodeGeneratorResponse_FEATURE_PROTO3_OPTIONAL))

	for _, protoFile := range req.GetProtoFile() {
		if !lo.Contains(req.FileToGenerate, *protoFile.Name) {
			continue
		}

		enums := protoFile.GetEnumType()
		if len(enums) == 0 {
			continue
		}

		// Derive Go package name from go_package option
		goPackageFull := protoFile.GetOptions().GetGoPackage()
		if goPackageFull == "" {
			continue
		}

		// go_package = "github.com/channel-io/ch-proto-public/coreapi/go/model" → "model"
		parts := strings.Split(goPackageFull, "/")
		goPackage := parts[len(parts)-1]

		// Output path: same as protoc-gen-go output, with _enum_openapi.go suffix
		// e.g. coreapi/go/model/group_enum_openapi.go
		protoFileName := *protoFile.Name
		// coreapi/model/group.proto → group
		baseName := strings.TrimSuffix(protoFileName[strings.LastIndex(protoFileName, "/")+1:], ".proto")

		// Build output path matching go_package structure
		// go_package path: github.com/channel-io/ch-proto-public/coreapi/go/model
		// We need relative path: coreapi/go/model/{baseName}_enum_openapi.go
		goPathParts := strings.Split(goPackageFull, "/")
		// Find "coreapi" index
		coreIdx := -1
		for i, p := range goPathParts {
			if p == "coreapi" {
				coreIdx = i
				break
			}
		}
		var outDir string
		if coreIdx >= 0 {
			outDir = strings.Join(goPathParts[coreIdx:], "/")
		} else {
			outDir = goPackage
		}

		outPath := fmt.Sprintf("%s/%s_enum_openapi.go", outDir, baseName)

		content, err := generateGoFile(protoFile, enums, goPackage)
		if err != nil {
			return err
		}

		resp.File = append(resp.File, &plugin.CodeGeneratorResponse_File{
			Name:    proto.String(outPath),
			Content: proto.String(content),
		})
	}

	return nil
}

func main() {
	req := &plugin.CodeGeneratorRequest{}
	resp := &plugin.CodeGeneratorResponse{}

	data, err := io.ReadAll(os.Stdin)
	if err != nil {
		logger.Fatal(err)
		return
	}

	if err := proto.Unmarshal(data, req); err != nil {
		logger.Fatal(err)
		return
	}

	if err := process(req, resp); err != nil {
		logger.Fatal(err)
		return
	}

	out, err := proto.Marshal(resp)
	if err != nil {
		logger.Fatal(err)
		return
	}

	os.Stdout.Write(out)
}
